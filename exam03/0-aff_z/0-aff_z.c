#include<unistd.h>

int main(int argc, char *argv[]){
        int i;
	i = 0;

	if(argc == 2){
 		while(argv[1][i]){
			if(argv[1][i] == 'z'){
				write(1, &argv[1][i], 1);
				break;
			}
			write(1, "a\n",2);
			i++;
		}
	}
         else{
		 write(1,"a\n", 2);
	 }
	 return (0);



}
